    int n = 10000;
    n = n - 2;
    int i, max = 0, mask;
    i = 0;
    mask = 1;
    while(i < 31) {
        if(n < mask)
        {
            max=i-1;
            break;
        }
        i = i + 1;
        mask = mask * 2;
    }
    int mtx[31][2][2];
    mtx[0][0][0] = 1;
    mtx[0][0][1] = 1;
    mtx[0][1][0] = 1;
    mtx[0][1][1] = 0;
    int ans[2][2] = {{1, 0}, {0, 1}};
